public class ThaiUnitCalculator {

        public static double calculateKrasobpuiToKg(double krasobpuiValue){
            return krasobpuiValue * 30;
        }

        public static double calculateKrasobtonnToKg(double krasobtonnValue){
            return krasobtonnValue * 50;
        }

        public static double calculateKrasobToKg(double krasobValue){
            return krasobValue * 100;
        }

        public static double calculateKwianToKg(double kwianValue){
            return kwianValue * 1000;
        }

        public static double calculatetung15ToKg(double tung15Value){
            return tung15Value * 15;
        }

        public static double calculatetung10ToKg(double tung10Value){
            return tung10Value * 10;
        }

        public static double calculateAumToKg(double aumValue){
            return aumValue * 2500;
        }

        public static double calculatePeepToKg(double peepValue){
            return peepValue * 10;
        }
    }
